namespace DataTypes
{
    public class Floating_PointTypes
    {
        // Ondalıklı Sayılar (Floating-Point Types)
        //
        // Ondalıklı sayılar, daha hassas ve geniş aralıklı sayılarla işlem yapabilmek için kullanılır.
        // Bunlar, matematiksel işlemlerde, özellikle bilimsel hesaplamalarda ve grafik uygulamalarında sıkça tercih edilir:
        
        
        // •	float: 32-bit (4 byte) boyutunda, yaklaşık 7 basamaklı duyarlılığa sahiptir. Ondalıklı sayılarla işlem yapmak için kullanılır. Örneğin: float pi = 3.14f;.
        // •	double: 64-bit (8 byte) boyutunda, yaklaşık 15-16 basamaklı duyarlılığa sahiptir. Varsayılan ondalıklı sayı türüdür. Örneğin: double e = 2.718281828;.
        // •	decimal: 128-bit (16 byte) boyutunda, yüksek hassasiyete sahip bir türdür. Finansal ve parasal işlemlerde kullanılır. Örneğin: decimal price = 99.99m;.
    }
}