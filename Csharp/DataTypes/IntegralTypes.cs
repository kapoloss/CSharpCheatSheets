namespace DataTypes
{
    public class IntegralTypes
    {
        // Tam Sayılar (Integral Types)
        //
        // C# dilinde tam sayılar, matematiksel işlemler ve diğer veri manipülasyonları için kullanılan türlerdir.
        // Her biri bellek boyutu ve işaretli/işaretsiz (signed/unsigned) özelliklerine göre ayrılır:
        
        
        // •	byte: 0 ile 255 arasında değer alabilen, 1 byte boyutundaki işaretsiz tam sayı türü.
        // •	sbyte: -128 ile 127 arasında değer alabilen, 1 byte boyutundaki işaretli tam sayı türü.
        // •	short: -32,768 ile 32,767 arasında değer alabilen, 2 byte boyutundaki işaretli tam sayı türü.
        // •	ushort: 0 ile 65,535 arasında değer alabilen, 2 byte boyutundaki işaretsiz tam sayı türü.
        // •	int: -2,147,483,648 ile 2,147,483,647 arasında değer alabilen, 4 byte boyutundaki işaretli tam sayı türü.
        // •	uint: 0 ile 4,294,967,295 arasında değer alabilen, 4 byte boyutundaki işaretsiz tam sayı türü.
        // •	long: -9,223,372,036,854,775,808 ile 9,223,372,036,854,775,807 arasında değer alabilen, 8 byte boyutundaki işaretli tam sayı türü.
        // •	ulong: 0 ile 18,446,744,073,709,551,615 arasında değer alabilen, 8 byte boyutundaki işaretsiz tam sayı türü.
        // •	nint ve nuint: Platform bağımlı türlerdir. Örneğin, 32-bit platformda 4 byte, 64-bit platformda 8 byte boyutundadır. İşaretli ve işaretsiz tam sayılar olarak kullanılır.
    }
}